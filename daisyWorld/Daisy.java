public class Daisy {
	
	private int color; // 0: white; 1: black.
	private int age; // current age of the daisy.

	public Daisy(int color, int age) {
		this.color = color;
		this.age = age;
	}

	// Getter and setter functions of variables.
	
	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	
}
