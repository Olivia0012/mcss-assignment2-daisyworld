public class Daisy {
	
	private int color;// 0: white; 1: black.
	private int age;

	public Daisy(int color, int age) {
		this.color = color;
		this.age = age;
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	
}
